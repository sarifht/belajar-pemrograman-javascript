// Break
for (i = 0; i < 10; i++) {
  if (i === 7) {
    break;
  }
  console.log(i);
}
console.log("i akan berhenti sebelum angka 7");
console.log("");

// Continue
for (i = 0; i < 10; i++) {
  if (i === 7) {
    continue;
  }
  console.log(i);
}
console.log("i akan melewati angka 7");
